def compress_text(text):
    if not text.strip():
        return text
    leading = re.match(r'^(\s*)', text).group(1)
    trailing = re.search(r'(\s*)$', text).group(1)
    core = text.strip()
    if not re.search(r'[\u4e00-\u9fff]', core):
        return text

    # Protect inline code
    codes = []
    def save(m):
        codes.append(m.group(0))
        return f"\x00C{len(codes)-1}\x00"
    core = re.sub(r'`[^`]+`', save, core)

    for pat, repl in RULES:
        try:
            core = re.sub(pat, repl, core)
        except Exception:
            pass

    def restore(m):
        i = int(m.group(1))
        return codes[i] if i < len(codes) else m.group(0)
    core = re.sub(r'\x00C(\d+)\x00', restore, core)

    return leading + core + trailing


def process_file(rel_path):
    inpath = os.path.join(INPUT_BASE, rel_path)
    outpath = os.path.join(OUTPUT_BASE, rel_path)
    os.makedirs(os.path.dirname(outpath), exist_ok=True)
    with open(inpath, 'r', encoding='utf-8') as f:
        lines = f.readlines()
    out = []
    in_code = False
    for line in lines:
        s = line.rstrip('\n')
        if s.startswith('```'):
            in_code = not in_code
            out.append(line); continue
        if in_code or not s.strip():
            out.append(line); continue
        if re.match(r'^#{1,6}\s', s) or re.match(r'^[-*_]{3,}\s*$', s):
            out.append(line); continue
        if s.startswith('>'):
            c = re.sub(r'^>\s?', '', s)
            out.append('> ' + compress_text(c) + '\n'); continue
        lm = re.match(r'^(\s*[-*+]\s|\s*\d+[.)]\s)(.*)', s)
        if lm:
            out.append(lm.group(1) + compress_text(lm.group(2)) + '\n'); continue
        out.append(compress_text(s) + '\n')
    with open(outpath, 'w', encoding='utf-8') as f:
        f.writelines(out)


def count_file_chars(path):
    with open(path, 'r', encoding='utf-8') as f:
        c = f.read()
    return len(c), count_cn(c)


def main():
    tb = ta = cb = ca = 0
    for rp in FILES:
        ip = os.path.join(INPUT_BASE, rp)
        op = os.path.join(OUTPUT_BASE, rp)
        bt, bc = count_file_chars(ip)
        process_file(rp)
        at, ac = count_file_chars(op)
        tb += bt; ta += at; cb += bc; ca += ac
        tp = (bt-at)/bt*100 if bt else 0
        cp = (bc-ac)/bc*100 if bc else 0
        print(f"  {rp}")
        print(f"    总: {bt}→{at} ({tp:.1f}%)  中: {bc}→{ac} ({cp:.1f}%)")
    print(f"\n{'='*60}")
    print(f"总计 (30 文件):")
    print(f"  总字符: {tb} → {ta} (减少 {(tb-ta)/tb*100:.1f}%)")
    print(f"  中文字符: {cb} → {ca} (减少 {(cb-ca)/cb*100:.1f}%)")

if __name__ == '__main__':
    main()
